document.querySelector('.functions');

operate = function(event, a, b){
    if (target.value = '+'){
        let result = a + b;
    }
    else if (target.value = '-'){
        let result = a - b;
    }
    else if (target.value = '*'){
        let result = a * b;
    }
    else if (target.value = '/'){
        let result = a / b;
    }
    else{console.log('error')};
}